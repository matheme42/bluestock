class ConditonsGenerale extends StatelessWidget {
  const ConditonsGenerale({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Scaffold(
        body: const SingleChildScrollView(
            child: Padding(
          padding: EdgeInsets.all(8.0),
          child: Text(cguString),
        )),
        bottomNavigationBar: Card(
            color: BluestockContext.of(context).cguAccepted.value == false
                ? Colors.deepPurple
                : Colors.grey,
            margin: const EdgeInsets.symmetric(horizontal: 20, vertical: 10),
            child: BluestockContext.of(context).cguAlreadyAccepted == false
                ? ListTile(
                    onTap: () {
                      SharedPreferenceController.setCgu().then((_) {
                        BluestockContext.of(context).cguAccepted.value = true;
                        BluestockContext.of(context).cguAlreadyAccepted = true;
                      });
                    },
                    title: Text(
                      'accept'.tr,
                      textAlign: TextAlign.center,
                      style: const TextStyle(color: Colors.white),
                    ),
                    visualDensity: const VisualDensity(vertical: -4),
                  )
                : ListTile(
                    onTap: () {
                      BluestockContext.of(context).cguAccepted.value = true;
                    },
                    title: Text(
                      'back'.tr,
                      textAlign: TextAlign.center,
                      style: const TextStyle(color: Colors.white),
                    ),
                    visualDensity: const VisualDensity(vertical: -4),
                  )));
  }
}
